public class Operations {

    public double addition (double parameterInput1, double parameterInput2) {
        double answer = 0;
        answer = parameterInput1 + parameterInput2;
        return answer;
    }

    public double subtraction (double parameterInput1, double parameterInput2) {
        double answer = 0;
        answer = parameterInput1 - parameterInput2;
        return answer;
    }

    public double multiplication (double parameterInput1, double parameterInput2) {
        double answer = 0;
        answer = parameterInput1 * parameterInput2;
        return answer;
    }

    public double division (double parameterInput1, double parameterInput2) {
        double answer = 0;
        answer = parameterInput1 / parameterInput2;
        return answer;
    }
}